<?php
	$arr = [
		[
			[1, 2, 3],
			[6, 7, 8],
			[3, 8, 4],
			[6, 7, 9],
		],
		[
			[9, 1, 2],
			[4, 5, 6],
		],
		[
			[9, 1, 2],
			[4, 5, 6],
			[5, 6, 3],
		],
	];
    $sum = 0;

    for ($i=0; $i < count($arr); $i++) { 
         for ($j=0; $j < count($arr[$i]); $j++) {
            for ($k=0; $k < count($arr[$i][$j]); $k++) { 
                $sum += $arr[$i][$j][$k];
            }
        }
    }

    print($sum);